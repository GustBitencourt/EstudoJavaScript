console.log("===============EXEMPLO WHILE=================");
function exemploWhile() {
    let num = 0;

    while(num <= 5) {
        console.log(num);
        num++;
    }
}

exemploWhile();

console.log("\n===============EXEMPLO DO WHILE=================");
function exemploDoWhile() {
    let num = 0

    do {
        console.log(num);
        num++;
    } while(num <= 5);
}

exemploDoWhile();

console.log("\n===============EXEMPLO DO WHILE2=================");
